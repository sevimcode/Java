package nestedloops;



public class Multiplication {  // multiplication table until 3

    public static void main ( String[] args ) {

        for (int i= 1; i<4; i++){
            for (int j= 1; j<11; j++){
                System.out.println (i + " X " + j + " = " + i*j);

            }
            System.out.println ();
            System.out.println ("--------------");
            System.out.println ();
        }



}
}
