package cybertekvariables;

public class OperationswithVariables {
    public static void main ( String[] args ) {
        System.out.println ("3"+ 1 + 5); // prints 315 after string it makes everting string
        System.out.println ("3"+ (1 + 2)); // prints 33
        System.out.println (5/4); // prints 1
        System.out.println (5.0 / 4); //prints 1.25  // java can make operation with every type of variable ex: it adds double int together
        System.out.println ('a'+ 5); // gets a 's  numeric value and add 5
        System.out.println ('a'); // prints a




    }


}
